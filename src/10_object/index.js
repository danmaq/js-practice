// 基本的なオブジェクトの構成
let cat = {
  name: 'Tama',
  legs: 4,
  tails: 1,
  enemies: ['Water', 'Dogs']
};


// オブジェクトにおける、プロパティへのアクセス例
console.log(cat.name); // Tama
console.log(cat.legs); // 4
console.log(cat.tails); // 1
console.log(cat.enemies); // [Water, Dogs]


